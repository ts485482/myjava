package ch03.sec04;

public class Ex10_BitLogicalExample {
    public static void main(String[] args) {
        //이진법 계산
        System.out.println("45 & 25 = " + (45 & 25));
        System.out.println("45 | 25 = " + (45 | 25));
        System.out.println("45 ^ 25 = " + (45 ^ 25));
        System.out.println("~45 = " + (~45));
        System.out.println("----------------------------------------");
    }
}
