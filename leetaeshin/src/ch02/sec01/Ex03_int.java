package ch02.sec01;

public class Ex03_int {
    public static void main(String[] args) {
        //int 기본 - 10진수
        int var1 = 18;
        
        //int 8진수 - 숫자 앞 0 < 8진수 선언
        int var2 = 022;

        //int 16진수 - 숫자 앞 0x < 16진수 선언
        int var3 = 0x12;

        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);
    }
}
