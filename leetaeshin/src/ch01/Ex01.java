package ch01;

public class Ex01 {
    public static void main(String[] args) {
        // 터미널 출력 구문 연습
        System.out.println("이태신");

        //4를 변수 a의 초기값으로 저장
        int a = 4;
        //5를 변수 b의 초기값으로 저장
        int b = 5;
        //변수 a와 b를 읽고, 두 변수를 더하는 산술연산 진행
        //연산의 결과값을 변수 c의 초기값으로 저장
        int c = a + b;

        //변수 c의 값을 읽고 콘솔에 출력
        System.out.println(c);


        int e = 10;
        int d = 11;
        int f = c + d + e;
        System.out.println("c + d + e = " + f);


    }
}
